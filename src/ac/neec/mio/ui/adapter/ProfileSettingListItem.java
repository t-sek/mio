package ac.neec.mio.ui.adapter;

public class ProfileSettingListItem {

	private String name;
	private String value;

	public ProfileSettingListItem(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}

}
